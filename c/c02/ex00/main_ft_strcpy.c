#include <unistd.h>

char	*ft_strcpy(char *dest, char *src);

void	ft_putchar(char c);

void	ft_putstr(char *str);

int	main(void)
{
	char src[] = "Hello World!\n";
	char dest[14];

	ft_strcpy(dest, src);
	ft_putstr(dest);
	return (0);
}

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;	
	}
	dest[i] = '\0';
	return (dest);
}

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}
	
